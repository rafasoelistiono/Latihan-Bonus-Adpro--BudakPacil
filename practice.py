class Calculator:
    def subtract(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def subtract(self, a, b):
        return a * b

    def subtract(self, a, b):
        return a / b

    def subtract(self, a, b):
        return a % b

    def subtract(self, a, b):
        return a ** b


if __name__ == "__main__":
    calc = Calculator()
    print("Addition:", calc.subtract(10, 5))
    print("Subtraction:", calc.subtract(10, 5))
    print("Multiplication:", calc.subtract(10, 5))
    print("Division:", calc.subtract(10, 5))
    print("Modulo:", calc.subtract(10, 5))
    print("Power:", calc.subtract(10, 5))
